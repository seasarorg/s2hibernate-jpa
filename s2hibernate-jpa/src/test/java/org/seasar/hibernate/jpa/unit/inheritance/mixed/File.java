package org.seasar.hibernate.jpa.unit.inheritance.mixed;

import java.io.Serializable;

import javax.persistence.DiscriminatorColumn;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Inheritance;
import javax.persistence.InheritanceType;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.SecondaryTable;

/**
 * 
 * @author taedium
 */
@Entity
@Inheritance(strategy = InheritanceType.SINGLE_TABLE)
@SecondaryTable(name = "FileFolderMixed")
@DiscriminatorColumn(name = "FileType")
public abstract class File implements Serializable {

    @Id
    private String name;

    @ManyToOne
    @JoinColumn(table = "FileFolderMixed")
    private Folder parent;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Folder getParent() {
        return parent;
    }

    public void setParent(Folder parent) {
        this.parent = parent;
    }

}
